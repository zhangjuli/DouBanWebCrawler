package webCrawler;

public class Book{
	String name;
	double rank;
	int pl;
	String author;
	String publish;
	String date;
	String price;
	public Book(String name, double rank, int pl, String author, String publish, String date, String price){
		this.name = name;
		this.rank = rank;
		this.pl = pl;
		this.author = author;
		this.publish = publish;
		this.date = date;
		this.price = price;
	}
}